--Employee Revenue

SELECT
e.employee_name,
SUM(sod.quantity*sod.unit_price) revenue
FROM Employees e
JOIN Sales_Orders so
ON e.employee_id=so.employee_id
JOIN Sales_Order_Details sod
ON so.order_id=sod.order_id
GROUP BY e.employee_name
ORDER BY revenue DESC;

