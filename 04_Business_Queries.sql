-- Business Analysis

Revenue by category.

select category_name,sum(quantity*unit_price) as revenue_by_category
from products p
inner join sales_order_details so
on so.product_id=p.product_id
inner join categories c
on p.category_id=c.category_id
group by category_name
order by revenue_by_category desc;

--Orders by employee.
select e.employee_name,e.employee_id,count(so.order_id) as orders_by_employee
from sales_orders so
inner join employees e
on so.employee_id=e.employee_id
group by e.employee_name,e.employee_id
order by orders_by_employee desc;

--Sales by state.

select r.region_name,sum(quantity*unit_price) as sales_by_state
from sales_orders o
inner join sales_order_details so
on o.order_id=so.order_id
inner join regions r
on o.region_id=r.region_id
group by r.region_name
order by sales_by_state desc;

--Monthly sales summary

select to_char(o.order_date,'MM'),sum(quantity*unit_price) as sales_by_state
from sales_orders o
inner join sales_order_details so
on o.order_id=so.order_id
inner join regions r
on o.region_id=r.region_id
group by to_char(o.order_date,'MM')
order by sales_by_state desc;


--Categories with revenue greater than ?5,00,000.


select category_name,sum(quantity*unit_price) as revenue_by_category
from products p
inner join sales_order_details so
on so.product_id=p.product_id
inner join categories c
on p.category_id=c.category_id
group by category_name
