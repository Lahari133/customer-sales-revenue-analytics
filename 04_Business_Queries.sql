--Average order value by customer.
SELECT customer_id,
       customer_name,
       AVG(order_total) AS avg_order_value
FROM (
    SELECT so.order_id,
           c.customer_id,
           c.customer_name,
           SUM(s.quantity * s.unit_price) AS order_total
    FROM sales_orders so
    JOIN sales_order_details s
      ON so.order_id = s.order_id
    JOIN customers c
      ON so.customer_id = c.customer_id
    GROUP BY so.order_id,
             c.customer_id,
             c.customer_name
)
GROUP BY customer_id, customer_name
ORDER BY avg_order_value DESC;


--Top 5 customers by total spending.
select customer_id,customer_name,total_spend
from
(
select c.customer_id,c.customer_name,sum(p.amount) as total_spend
from sales_orders s
inner join payments p
on s.order_id=p.order_id
inner join customers c
on s.customer_id=c.customer_id
group by c.customer_id,c.customer_name
order by total_spend desc
)
where rownum<=5;


